// Pre incre/decre operator(++i/--i)

#include<iostream>
using namespace std;
int main(){
    int i=4;
    int a=++i;
    // pehle incre/decre
    cout<<"i = "<<i;
    cout<<"a = "<<a;
    cout<<"\n";

    int j=4;
    int b=5;
    int sum=b+(--j);
    cout<<"j = "<<j;
    cout<<"sum = "<<sum;

}

